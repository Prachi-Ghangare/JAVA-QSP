class ConstantsValue {
    public static void main(String[] args) {
        double pi = 3.14;
        double sqRoot2 = 1.414;
        double sqRoot3 = 1.732;
        double euler = 0.577;
        double omega = 0.567;
        double gauss = 0.835;
        System.out.println("Value of pi - " +pi);
        System.out.println("Value of Square Root of 2 - " +sqRoot2);
        System.out.println("Value of Square Root of 3 - " +sqRoot3);
        System.out.println("Value of Euler's constant - " +euler);
        System.out.println("Value of Omega - " +omega);
        System.out.println("Value of Gauss's constant - " +gauss);
    }
}
